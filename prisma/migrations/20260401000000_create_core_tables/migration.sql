-- profiles: users can only read/update their own profile
-- INSERT is handled by the handle_new_user trigger (auth.users creates profile automatically)
CREATE POLICY "profiles_select_own" ON "profiles"
    FOR SELECT USING (auth.uid()::text = user_id);

CREATE POLICY "profiles_update_own" ON "profiles"
    FOR UPDATE USING (auth.uid()::text = user_id);

-- searches: scoped to owner; guests can insert with null user_id
CREATE POLICY "searches_select_own" ON "searches"
    FOR SELECT USING (auth.uid()::text = user_id);

CREATE POLICY "searches_insert" ON "searches"
    FOR INSERT WITH CHECK (auth.uid()::text = user_id OR user_id IS NULL);

CREATE POLICY "searches_update_own" ON "searches"
    FOR UPDATE USING (auth.uid()::text = user_id);

-- contacts: scoped via parent search ownership
-- Guest contacts (search.user_id IS NULL) are accessible without auth
CREATE POLICY "contacts_select_own" ON "contacts"
    FOR SELECT USING (
        auth.uid()::text = (SELECT user_id FROM searches WHERE id = search_id)
    );

CREATE POLICY "contacts_insert_own" ON "contacts"
    FOR INSERT WITH CHECK (
        auth.uid()::text = (SELECT user_id FROM searches WHERE id = search_id)
        OR (SELECT user_id FROM searches WHERE id = search_id) IS NULL
    );

CREATE POLICY "contacts_update_own" ON "contacts"
    FOR UPDATE USING (
        auth.uid()::text = (SELECT user_id FROM searches WHERE id = search_id)
    );

-- outreach: scoped via contact -> search ownership chain
CREATE POLICY "outreach_select_own" ON "outreach"
    FOR SELECT USING (
        auth.uid()::text = (
            SELECT s.user_id FROM contacts c
            JOIN searches s ON s.id = c.search_id
            WHERE c.id = contact_id
        )
    );

CREATE POLICY "outreach_insert_own" ON "outreach"
    FOR INSERT WITH CHECK (
        auth.uid()::text = (
            SELECT s.user_id FROM contacts c
            JOIN searches s ON s.id = c.search_id
            WHERE c.id = contact_id
        )
    );

CREATE POLICY "outreach_update_own" ON "outreach"
    FOR UPDATE USING (
        auth.uid()::text = (
            SELECT s.user_id FROM contacts c
            JOIN searches s ON s.id = c.search_id
            WHERE c.id = contact_id
        )
    );

