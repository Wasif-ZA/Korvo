-- ============================================
-- PROFILE AUTO-CREATION TRIGGER
-- Fires when a new user signs up via Supabase Auth (Google OAuth)
-- Creates the profiles row automatically — no client-side code needed
-- SECURITY DEFINER runs as function owner (bypasses RLS) — intentional for initial INSERT
-- ============================================

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
    INSERT INTO public.profiles (
        id,
        user_id,
        email,
        full_name,
        avatar_url,
        plan,
        searches_used_this_month,
        drafts_used_this_month,
        searches_reset_at,
        created_at,
        updated_at
    ) VALUES (
        gen_random_uuid()::text,
        NEW.id::text,
        NEW.email,
        NEW.raw_user_meta_data->>'full_name',
        NEW.raw_user_meta_data->>'avatar_url',
        'free',
        0,
        0,
        NOW(),
        NOW(),
        NOW()
    );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();
